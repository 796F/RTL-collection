void printData(u8 *data, u8 size)
{
  int i;
  for (i=0; i<size; i++)
  {
    printf("%02X", data[i]);
    if ((i+1)%8 == 0) printf(" ");
    if ((i+1)%32 == 0) printf("\n");
  }
  printf("\n");
}

